/*
Assignment Operators
*/

let x = 20;
x += 1;
console.log(x); // x = x +1

x -=1;
console.log(x); // x = x -1

x *=2;
console.log(x); // x = x*1

x /=2;
console.log(x); // x = x/1

x **=2;
console.log(x); // x = x ^2

x %=3;
console.log(x);

